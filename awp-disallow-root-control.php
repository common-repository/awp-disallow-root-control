// Add settings to the "Reading" settings page
function awpdrc_register_settings() {
    add_settings_section(
        'awpdrc_settings_section', // Section ID
        __( 'Disallow Root Control', 'awp-disallow-root-control' ), // Section Title with translation
        'awpdrc_section_callback', // Callback function to output the section description
        'reading' // The settings page to display the section
    );

    add_settings_field(
        'awpdrc_enable_disallow', // Field ID
        __( 'Enable Disallow Root in Robots.txt', 'awp-disallow-root-control' ), // Field Title with translation
        'awpdrc_enable_disallow_callback', // Callback function to output the field
        'reading', // The settings page to display the field
        'awpdrc_settings_section' // The section to display the field
    );

    register_setting( 'reading', 'awpdrc_enable_disallow', array(
        'type' => 'boolean',
        'description' => __( 'Enable disallowing the root directory in robots.txt', 'awp-disallow-root-control' ),
        'sanitize_callback' => 'awpdrc_sanitize_checkbox',
        'default' => false,
    ) );
}
add_action( 'admin_init', 'awpdrc_register_settings' );

// Output the section description
function awpdrc_section_callback() {
    echo '<p>' . esc_html__( 'Control access to the root directory in the robots.txt file.', 'awp-disallow-root-control' ) . '</p>';
}

// Output the field
function awpdrc_enable_disallow_callback() {
    $option = get_option( 'awpdrc_enable_disallow' );
    ?>
    <input type="checkbox" name="awpdrc_enable_disallow" value="1" <?php checked( 1, $option ); ?> />
    <label for="awpdrc_enable_disallow"><?php esc_html_e( 'Enable disallowing the root directory in robots.txt', 'awp-disallow-root-control' ); ?></label>
    <?php
}

// Sanitize the checkbox input
function awpdrc_sanitize_checkbox( $input ) {
    return ( $input === '1' ) ? 1 : 0;
}

// Generate robots.txt content based on the setting
function awpdrc_generate_robots_txt() {
    $enable_disallow = get_option( 'awpdrc_enable_disallow' );

    if ( $enable_disallow ) {
        $robots_txt = "User-agent: *\n";
        $robots_txt .= "Disallow: /\n";
        $robots_txt .= "Allow: /wp-admin/admin-ajax.php\n";

        status_header( 200 );
        header( 'Content-Type: text/plain; charset=utf-8' );
        echo "<!-- Robots.txt generated by AWP Disallow Root Control plugin -->\n";
        echo esc_html( $robots_txt );
        exit;
    }
}
add_action( 'do_robotstxt', 'awpdrc_generate_robots_txt' );